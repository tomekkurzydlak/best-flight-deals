class FlightData:

    def __init__(self, price, orig_city, orig_airport, dest_city, dest_airport, out_date, return_date):
        self.price = price
        self.orig_city = orig_city
        self.orig_airport = orig_airport
        self.dest_city = dest_city
        self.dest_airport = dest_airport
        self.out_date = out_date
        self.return_date = return_date
